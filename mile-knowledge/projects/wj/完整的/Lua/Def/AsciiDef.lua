function AsciiDef_CharWidth_26(code)
	local nFontType = FontMgr.GetCurFont(FontID.Default)
	if nFontType == FontType.Default then
		return Font_Default_AsciiCharWidth_26[code]
	elseif nFontType == FontType.FZHT then
		return Font_FZHT_AsciiCharWidth_26[code]
	elseif nFontType == FontType.XMSANS then
		return Font_XMSANS_AsciiCharWidth_26[code]
	end

	return 10
end