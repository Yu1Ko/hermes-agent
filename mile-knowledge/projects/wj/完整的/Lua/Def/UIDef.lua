GameSetting_Version_Updater = {
    [SettingCategory.ShortcutInteraction] = function(nOldVer, nNewVer)
        local tStoreSetting = UISettingStoreTab.ShortcutInteraction
        local tSetting = clone(UISettingStoreTabDefault.ShortcutInteraction)

        local tStoreDefKeyMap = {}
        for k, v in pairs(tStoreSetting) do
            --NOTE: szDef是后来才加的，这里做下兼容
            tStoreDefKeyMap[v.szDef or v.szName] = v.VKey
        end

        local tNewShortcut = {}
        for k, v in pairs(tSetting) do
            local nShortcutDef = ShortcutDef[v.szDef]
            local VKey = tStoreDefKeyMap[v.szDef] or tStoreDefKeyMap[v.szName]
            if VKey then
                tSetting[k].VKey = VKey
            else
                tNewShortcut[k] = tSetting[k].VKey
            end

            --旧版设置项改名后，将老设置覆盖到新设置上（之后新版引入了szDef，名字修改后无需再手动操作覆盖老设置）：
            --2024.8.26 ClipCursor更名：限制鼠标活动范围->控制鼠标不可离开游戏窗口，Version: 2->3
            if nShortcutDef == ShortcutDef.ClipCursor and tStoreDefKeyMap["限制鼠标活动范围"] then
                tSetting[k].VKey = tStoreDefKeyMap["限制鼠标活动范围"]
            end

            --默认键位修改后，将新默认键位覆盖
            for _, tLine in ipairs(Default_Shortcut_Update) do
                if nShortcutDef == tLine.nShortcutDef and nOldVer < tLine.nVersion and tSetting[k].VKey == tLine.szDefault then
                    tSetting[k].VKey = UISettingStoreTabDefault.ShortcutInteraction[k].VKey
                    break
                end
            end
        end

        --新增的快捷键若已被占用则置空
          for k, v in pairs(tSetting) do
            for nID, VKey in pairs(tNewShortcut) do
                if nID ~= k and VKey == v.VKey then
                    if not ShortcutInteractionData._isDXSkillShortcutkey(tSetting[nID].szDef) and
                        not ShortcutInteractionData._isDXSkillShortcutkey(tSetting[k].szDef) then
                        tSetting[nID].VKey = ""
                    end
                end
            end
        end

        UISettingStoreTab.ShortcutInteraction = tSetting
    end,
    [SettingCategory.GamepadInteraction] = function(nOldVer, nNewVer)
        local tStoreSetting = UISettingStoreTab.GamepadInteraction
        local tSetting = clone(UISettingStoreTabDefault.GamepadInteraction)

        local tStoreDefKeyMap = {}
        for k, v in pairs(tStoreSetting) do
            --NOTE: szDef是后来才加的，这里做下兼容
            tStoreDefKeyMap[v.szDef or v.szName] = v.VKey
        end

        local tNewShortcut = {}
        for k, v in pairs(tSetting) do
            local VKey = tStoreDefKeyMap[v.szDef] or tStoreDefKeyMap[v.szName]
            if VKey then
                tSetting[k].VKey = VKey
            else
                tNewShortcut[k] = tSetting[k].VKey
            end

            --旧版设置项改名后，将老设置覆盖到新设置上：
            --2024.10.10 LockTarget更名：锁定目标->目标锁定，Version: 1->2
            if GamepadDef[v.szDef] == GamepadDef.LockTarget and tStoreDefKeyMap["锁定目标"] then
                tSetting[k].VKey = tStoreDefKeyMap["锁定目标"]
            end
        end

        --新增的快捷键若已被占用则置空
        for k, v in pairs(tSetting) do
            for nID, VKey in pairs(tNewShortcut) do
                if nID ~= k and VKey == v.VKey then
                    tSetting[nID].VKey = ""
                end
            end
        end

        UISettingStoreTab.GamepadInteraction = tSetting
    end,
}

